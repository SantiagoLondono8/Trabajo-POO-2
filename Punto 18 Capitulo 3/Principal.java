    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        // TODO add your handling code here:
        //Respuesta siguiente =new Respuesta();
        //siguiente.show(true);
        String nombre, codigo;
        nombre = txtNombre.getText();
        codigo = txtCodigo.getText();
        double horas, precio, porcentaje, salarioneto, salariobruto;
        horas = Double.parseDouble(txtHoras.getText());
        precio = Double.parseDouble(txtPrecio.getText());
        porcentaje = Double.parseDouble(txtRetefuente.getText());
        salariobruto = horas * precio;
        salarioneto = salariobruto - (salariobruto * (porcentaje / 100));
        JOptionPane.showMessageDialog(this, "Nombre del empleado: " + nombre + "\nCodigo del empleado: " + codigo + "\nSalario bruto: " + salariobruto + "\nSalario neto: " + salarioneto);
    }//GEN-LAST:event_btnContinuarActionPerformed

